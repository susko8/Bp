package com.samuel.altzasuvkaapp;


public class DataEntry
{
    private double valX;
    private double valY;
    DataEntry(double x, double y)
    {
        valX=x;
        valY=y;
    }
    public double getValX()
    {
        return valX;
    }

    public double getValY()
    {
        return valY;
    }
}
